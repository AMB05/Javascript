// Penjumlahan  
let a = 10;  
let b = 5;  
let penjumlahan = a + b; // 15  
console.log('Hasil Penjumlahan:', penjumlahan);  

// Pengurangan  
let pengurangan = a - b; // 5  
console.log('Hasil Pengurangan:', pengurangan);  

// Perkalian  
let perkalian = a * b; // 50  
console.log('Hasil Perkalian:', perkalian);  

// Pembagian  
let pembagian = a / b; // 2  
console.log('Hasil Pembagian:', pembagian);  

// Modulus (sisa bagi)  
let modulus = a % b; // 0  
console.log('Hasil Modulus:', modulus);  

// Contoh dengan angka desimal  
let c = 7.5;  
let d = 2.5;  
let penjumlahanDecimal = c + d; // 10  
console.log('Hasil Penjumlahan Desimal:', penjumlahanDecimal);


