// Penggabungan String  
let firstName = "John";  
let lastName = "Doe";  

// Menggunakan operator + untuk penggabungan  
let fullName = firstName + " " + lastName;  
console.log('Nama Lengkap:', fullName); // Output: Nama Lengkap: John Doe  

// Menggunakan template literal  
let fullNameTemplate = `${firstName} ${lastName}`;  
console.log('Nama Lengkap (Template Literal):', fullNameTemplate); // Output: Nama Lengkap (Template Literal): John Doe  

// Mengukur Panjang String  
let message = "Hello, World!";  
let lengthOfMessage = message.length; // Mengukur panjang string  
console.log('Panjang String:', lengthOfMessage); // Output: Panjang String: 13

