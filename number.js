let a = 10;  
let b = 5;  

console.log(a + b); // Output: 15 (penjumlahan)  
console.log(a - b); // Output: 5  (pengurangan)  
console.log(a * b); // Output: 50 (perkalian)  
console.log(a / b); // Output: 2  (pembagian)  
console.log(a % b); // Output: 0  (modulus)


// number
let str = "123";  
let num = Number(str);  
console.log(num); // Output: 123

// parseInt dan pareseFloat
let intValue = parseInt("42.5");    // Menghasilkan 42  
let floatValue = parseFloat("42.5"); // Menghasilkan 42.5  
console.log(intValue);   // Output: 42  
console.log(floatValue); // Output: 42.5


